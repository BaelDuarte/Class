# Escreva um programa que retorne o valor hora de um funcionário com base em seu salário mensal e horas trabalhadas por mês
#
salario_mensal = input('Qual é seu salário mensal?')

jornada_mensal = input('Quanto tempo você trabalha por mês?')

valor_horas = int(salario_mensal) / int(jornada_mensal)

print('o seu valor hora é igual a...')

print(valor_horas)


